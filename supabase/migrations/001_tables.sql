-- ─── Helper Function ─────────────────────────────────────────────────────────

create or replace function is_dashboard_admin()
returns boolean as $$
  select exists (
    select 1 from dashboard_users
    where id = auth.uid() and role = 'admin'
  );
$$ language sql security definer;
